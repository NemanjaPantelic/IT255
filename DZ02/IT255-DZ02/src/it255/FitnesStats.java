package it255;

public class FitnesStats {

    private Long steps;
    private Long stairs;
    private Float distance;

    public Long getSteps() {
        return steps;
    }

    public void setSteps(Long steps) {
        this.steps = steps;
    }

    public Long getStairs() {
        return stairs;
    }

    public void setStairs(Long stairs) {
        this.stairs = stairs;
    }

    public Float getDistance() {
        return distance;
    }

    public void setDistance(Float distance) {
        this.distance = distance;
    }

}
